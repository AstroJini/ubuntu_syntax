# 현재위치에서서 파일 및 폴더 조회
ls

# 목록 조회 자세히
ls -l

# 목록조회 자세히, 숨김파일까지
ls -al

# 목록조회 자세히, 숨김파일, 오래된 순 정렬 (최신은 alt)
ls -alrt (r = reverse , t = time)

